@app.get("/summary")
def get_summary():

    conn = sqlite3.connect("logs.db")

    cursor = conn.cursor()

    cursor.execute("""
    SELECT root_cause
    FROM incidents
    ORDER BY id DESC
    LIMIT 10
    """)

    rows = cursor.fetchall()

    conn.close()

    text = "\n".join(
        [row[0] for row in rows]
    )

    report = analyze_logs(text)

    return {
        "summary": report
    }

@app.get("/ask")
def ask_logs(question: str):

    if "count" in question.lower():

        return {
            "answer": f"Total indexed chunks: {len(documents)}"
        }

    results = search_logs(question)

    context = "\n".join(results[:3])
    prompt = f"""
You are an AI assistant.

Use ONLY the retrieved context below.

If the context is not a log file,
answer normally.

If it is a log file,
provide:
- Root Cause
- Impact
- Recommendation

Question:
{question}

Context:
{context}
"""
    answer = analyze_logs(prompt)

    return {
        "question": question,
        "answer": answer,
        "retrieved_logs": results[:3]
    }
