@app.post("/analyze-log")
async def analyze_log(file: UploadFile = File(...)):
    REQUEST_COUNT.inc()
    LOG_ANALYSIS_COUNT.inc()
    content = await file.read()
    try:
        text = content.decode("utf-8")
    except UnicodeDecodeError:
        text = content.decode("latin-1")

    ai_report = analyze_logs(text)
    print("\n========== LOG CONTENT ==========")
    print(text)
    print("=================================\n")
    lines = text.splitlines()

    # chunk and add to vector store
    for i in range(0, len(lines), 50):
        chunk = "\n".join(lines[i:i+50])
        if chunk.strip():
            emb = get_embedding(chunk)
            add_document(chunk, emb)

    severity = extract_severity(ai_report)
    save_incident(
        file.filename,
        severity,
        ai_report[:500],
        "Generated by Llama3"
    )

    errors = []
    warnings = []
    infos = []
    for line in lines:
        if "ERROR" in line:
            errors.append(line)
        elif "WARNING" in line:
            warnings.append(line)
        elif "INFO" in line:
            infos.append(line)

    ERROR_COUNT.inc(len(errors))

    return JSONResponse({
        "summary": {
            "total_lines": len(lines),
            "errors": len(errors),
            "warnings": len(warnings),
            "infos": len(infos)
        },
        "ai_report": ai_report,
        "error_logs": errors[:10],
        "warning_logs": warnings[:10]
    })