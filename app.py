def save_incident(filename, severity, root_cause, recommendation):

    conn = sqlite3.connect("logs.db")

    cursor = conn.cursor()

    cursor.execute("""
    INSERT INTO incidents(
        filename,
        severity,
        root_cause,
        recommendation
    )
    VALUES(?,?,?,?)
    """,(
        filename,
        severity,
        root_cause,
        recommendation
    ))

    conn.commit()
    conn.close()
